import type { ViralPromptInput } from './types';
import { pickRandom, randomInt } from '@/shared/lib/random';

// ===== "졸업 후기형" 프롬프트 =====
// 레퍼런스: 난임 카페 졸업글, 다이어트 성공글, 수술 회복 완료글 등
// 핵심: 고비를 넘긴 사람이 겸손하게 경험을 공유하며 다른 회원들을 응원하는 글

const GRADUATION_CONTEXTS: Record<string, string> = {
  난임: `만 N세 고령, 시험관 N차 만에 착상 성공. 아직 초기라 조심스럽지만 그동안 카페에서 도움 많이 받아서 졸업글 남김. 생활습관/영양제/운동/식단 정리.`,
  산후조리: `둘째 출산 후 이번엔 제대로 산후조리 해서 첫째 때와 확연히 다른 결과. 3개월 기록 정리하며 졸업.`,
  갱년기: `갱년기 진단 후 1년, 호르몬 수치 안정화되고 증상 많이 줄어듦. 도움 된 것들 정리.`,
  기력보충: `만성 피로로 2년 고생하다 6개월 관리 후 건강검진 수치 정상화. 직장 복귀 후기.`,
  다이어트: `N kg 감량 성공. 요요 없이 유지 중. 식단+운동+보충제 정리.`,
};

const getGraduationContext = (keyword: string): string => {
  for (const [key, ctx] of Object.entries(GRADUATION_CONTEXTS)) {
    if (keyword.includes(key)) return ctx;
  }
  return `"${keyword}" 관련 오랜 고민 끝에 일정 수준 성과를 이룬 사람이 겸손하게 경험 공유하며 졸업.`;
};

// 흑염소 등장 위치 (영양제/보양식 리스트의 한 항목으로)
const GOAT_IN_LIST_ROUTES = [
  '영양제 리스트 중 하나로 등장 — "흑염소진액 (브랜드명 가림) — 기력 보충 목적으로 추가"',
  '식단 항목에서 등장 — "보양식으로 흑염소진액 아침에 한 포씩 챙겨 먹음"',
  '마지막 차수에 추가한 것 중 하나로 — "이번에 추가로 흑염소진액 병행"',
  '친정엄마/시어머니가 챙겨줘서 추가 — "어머니가 보내주신 흑염소진액도 같이 먹었음"',
];

export const buildGraduationPrompt = (input: ViralPromptInput): string => {
  const { keyword } = input;
  const context = getGraduationContext(keyword);
  const goatRoute = pickRandom(GOAT_IN_LIST_ROUTES);

  const listCount = randomInt(6, 8);

  return `너는 네이버 카페에 "졸업 후기"를 올리는 실제 회원이야.

오랫동안 고민하고 노력해서 일정 수준 성과를 이룬 사람이 겸손하게 경험을 공유하는 글.
"아직 끝이 아니지만, 카페에서 많은 도움을 받았으니 저도 누군가에게 도움이 되고 싶어서" 톤.

## 절대 금지
1. 효능 단정 금지 — "~덕분에 성공했다" 금지. "도움이 됐는지는 모르겠지만 꾸준히 했어요" 수준
2. 마크다운 헤더/볼드 금지 (텍스트 표 OK)
3. 이모지는 졸업글 특성상 2~3개만 허용 (😊, 😄 정도)
4. 닉네임/해시태그 금지 (댓글 태그만)
5. 프롬프트의 어떤 예시 문장도 복사 금지. 100% 새로 창작.

## 키워드: ${keyword}
## 상황: ${context}

## 흑염소 등장: ${goatRoute}
★ 흑염소는 영양제/보양식 리스트의 한 항목으로만 등장. 특별하게 강조하지 않음. 다른 항목들(엽산, 비타민D, 오메가3 등)과 동일 비중.
★ 브랜드명은 가림 — 다른 영양제는 브랜드 공개해도 흑염소만 브랜드를 가리고 궁금하면 댓글로 소통하는 형태. 단, 가리는 표현을 매번 새롭게 창작할 것. 프롬프트의 표현 복사 금지.

## 글 구조

[도입]
- 간결한 자기소개 (나이, 상황, 기간)
- "아직 끝이 아니지만" 겸손한 톤
- "카페에서 도움 많이 받아서 조심스럽게 졸업글"

[평소 생활습관] (번호 리스트 ${listCount}개)
- 1. 수면  2. 운동  3. 식단  4. 영양제(구체적 제품명+용량)  5. 기타 관리  6. 금지한 것들
- 각 항목 2~5줄로 간결하게
- 영양제는 제품명+용량까지 구체적으로 (레퍼런스처럼)
- 흑염소는 이 리스트의 한 항목으로 자연스럽게

[마지막에 추가로 한 것]
- 성공한 차수/시기에 특별히 더 한 것 2~3개

[마무리]
- "개인마다 달라서 정답은 없지만" 겸손
- "카페에 계신 모든 분들에게 좋은 소식이 오길" 응원
- 이모지 1~2개 허용 (😊 정도)

## 톤
- 겸손 + 감사 + 응원
- 장황하지 않고 정돈된 리스트형 (블로그 톤은 아니지만 정리는 깔끔)
- 화자 나이에 맞는 말투

## 분량
- 본문 2000~3000자 (졸업글은 장문이 아님. 핵심만 정리.)
- 줄바꿈 적절히

## 댓글 특징 (졸업글 댓글은 다른 유형과 완전히 다름)
- 축하 댓글이 주류 (50% 이상)
- 영양제/운동/병원 구체 질문 (30%)
- [작성자-N] 답글이 매우 많음 — 거의 모든 질문에 친절하게 하나하나 답변
- 댓글 톤: "축하드려요!", "좋은 기운 받아갈게요", "저도 열심히 해야겠네요"
- 경험 공유 댓글: "저도 ~했더니 이번에 ~됐어요!"

## 출력 형식
[제목] (20~40자, "졸업글" 느낌)
[본문] (2000~3000자)
[댓글] (30~50개, [댓글N]/[작성자-N]/[댓글러-N]/[제3자-N] 태그만)

바로 [제목]부터 시작.`;
};
