const detectSynergyCategory = (keyword: string): string => {
  const map: [RegExp, string][] = [
    [/씬 프로|다이어트|이소비텍신/, '다이어트'],
    [/두유|전두유|베지밀|프로틴|단백/, '단백질'],
    [/유산균|락토핏/, '유산균'],
    [/오메가|프로메가/, '오메가3'],
    [/비타민D|비타민K|종합.*비타민/, '비타민'],
    [/루테인|지아잔틴/, '루테인'],
    [/글루타치온/, '글루타치온'],
    [/면역|하이뮨/, '면역'],
    [/구강|덴티/, '구강'],
    [/흑염소|마가목/, '흑염소'],
  ];
  for (const [re, cat] of map) {
    if (re.test(keyword)) return cat;
  }
  return '기본';
};

