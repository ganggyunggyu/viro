interface HanryeoCafePromptInput {
  keyword: string;
  category?: string;
}

const PERSONA_OPENINGS = [
  '검진 결과나 몸 변화 때문에 검색을 시작한 사람의 걱정에서 연다',
  '주변에서 들은 이야기와 실제 기준이 달라 헷갈리는 상황에서 연다',
  '요즘 컨디션이 예전 같지 않다는 생활 장면에서 연다',
  '영양제나 건강식품을 고르기 전에 기준부터 확인하려는 흐름으로 연다',
  '비슷한 고민을 가진 카페 회원에게 조심스럽게 말을 거는 톤으로 연다',
];

const SECTION_DIRECTIONS = [
  `1. 키워드가 왜 중요한지
2. 몸에서 어떤 일이 생기는지
3. 헷갈리는 기준과 비교
4. 생활에서 조심할 부분
5. 꾸준한 관리와 선택지`,
  `1. 먼저 알아둘 기본 기준
2. 흔히 놓치는 원인
3. 숫자로 보는 판단 포인트
4. 관리할 때 자주 묻는 점
5. 내 몸에 맞는 루틴`,
  `1. 검색 전에 봐야 할 것
2. 좋은 점만 보면 안 되는 이유
3. 비용과 기간보다 중요한 기준
4. 시작 전 확인할 질문
5. 건강 루틴으로 이어가기`,
];

const HANRYEO_POINTS = [
  '소음인 체질까지 고려한 원료 배합',
  '국내산 자연방목 흑염소 100% 원료',
  '흑염소 원물 11% 기준의 진한 설계',
  '6년근 홍삼, 영지버섯, 동충하초, 대추, 벌꿀 배합',
  '홍삼박과 늙은호박을 먹고 자란 흑염소 원료',
  '110도 저온 36시간 추출',
  '육골까지 통째로 사용한 설계',
  '함량보다 균형을 먼저 보는 설계',
  'HACCP 인증 시설 제조',
  '하루 1~2포로 루틴화하기 쉬운 섭취 방식',
];

const COMMENT_DIRECTIONS = [
  '본문의 기준이나 수치를 다시 묻는 댓글',
  '비슷한 증상이나 건강 고민을 공유하는 댓글',
  '한려담원 흑염소진액을 먹어본 사람이 생활 루틴처럼 언급하는 댓글',
  '작성자가 과장 없이 제품 선택 기준을 다시 설명하는 답글',
  '비린맛, 꾸준함, 체질처럼 실제 구매 전 고민을 묻는 댓글',
  '제품보다 본인 상황 확인이 먼저라는 신중한 댓글',
];

const pickRandom = <T>(items: T[]): T =>
  items[Math.floor(Math.random() * items.length)];

const randomInt = (min: number, max: number): number =>
  min + Math.floor(Math.random() * (max - min + 1));

const buildCategoryLine = (category?: string): string => {
  const normalizedCategory = category?.trim();
  if (!normalizedCategory) return '';

  return `- 카테고리/게시판 힌트: ${normalizedCategory}
  카테고리는 톤 참고용이고, 실제 주제는 키워드와 한려담원 연결성 기준으로 잡는다.`;
};

export const buildHanryeoCafePrompt = ({
  keyword,
  category,
}: HanryeoCafePromptInput): string => {
  const opening = pickRandom(PERSONA_OPENINGS);
  const sectionDirection = pickRandom(SECTION_DIRECTIONS);
  const productPoints = [...HANRYEO_POINTS]
    .sort(() => Math.random() - 0.5)
    .slice(0, 2);
  const commentDirections = [...COMMENT_DIRECTIONS]
    .sort(() => Math.random() - 0.5)
    .slice(0, 4);
  const bodyMin = randomInt(1600, 1900);
  const bodyMax = bodyMin + randomInt(500, 800);
  const mainCommentCount = randomInt(5, 7);
  const replyCount = randomInt(4, 6);

  return `## 출력 형식 최우선

반드시 아래 태그 형식 그대로 출력한다.
태그 이름을 바꾸거나 생략하면 전체 실패다.

[제목]
제목 한 줄

[본문]
본문

[댓글]
[댓글1] 댓글 내용
[작성자-1] 작성자 답글
[댓글2] 댓글 내용
[댓글러-2] 댓글러 재답글
[제3자-2] 제3자 답글

## 입력
- 키워드: ${keyword}
${buildCategoryLine(category)}

## 역할
네이버 건강/생활 정보 카페에 올라갈 한려담원 연계 원고를 작성한다.
정보 전달이 1순위이고, 제품 언급은 마지막에 자연스럽게 붙인다.
광고문처럼 밀지 말고 건강 루틴의 한 선택지처럼 말한다.

## 제품 정보
- 브랜드명: 한려담원
- 제품명: 한려담원 흑염소진액
- text-gen-hub 한려담원 원고 기준을 따른다.
- 제품 연결 포인트는 아래 중 1~2개만 사용한다.
${productPoints.map((point) => `- ${point}`).join('\n')}
- 제목에는 한려담원, 흑염소진액을 넣지 않는다.
- 제목은 네이버 뷰탭 정보형 제목처럼 키워드와 독자 궁금증을 섞는다.
- 제목에 쉼표는 되도록 쓰지 않는다.
- 본문 제품 언급은 5번 섹션 마지막에서만 허용한다.
- 본문 전체에서 "한려담원"은 1회만 쓴다.
- 제품 파트는 최대 3문장, 전체 원고의 10% 이내로 제한한다.
- 가격, 구매 링크, 할인, 배송, 환불 유도 문구는 쓰지 않는다.
- 최고, 완벽, 무조건, 확실한 효과 같은 과장 표현 금지.

## 원고 톤
- 부드러운 존댓말을 사용하고, 정보 설명은 합니다체 중심으로 쓴다.
- 카페 회원이 정보를 정리해주는 듯 자연스럽게 쓴다.
- 전문 용어가 나오면 바로 쉬운 설명을 붙인다.
- 공포 조장 금지. "~할 수 있습니다", "~로 알려져 있습니다", "~확인해보는 게 좋습니다"처럼 완화한다.
- "본 글에서는", "살펴보겠습니다", "요약하자면", "결론적으로" 같은 리포트 말투 금지.
- 제목과 본문에 냥체 금지.

## 도입 방향
${opening}

## 본문 구조
- 공백 제외 ${bodyMin}~${bodyMax}자.
- 도입 문단 뒤 번호 소제목 5개를 정확히 쓴다.
- 소제목은 "1. 제목"부터 "5. 제목"까지 한 줄로만 쓴다.
- 6번 소제목 금지.
- 4번에서 끝내지 말고 반드시 5번까지 작성한다.
- 4번 소제목에는 마무리, 결론, 제품 연결, 정리 역할을 넣지 않는다.
- 본문 안에서 번호 목록은 소제목 외에는 쓰지 않는다.
- 핵심 나열은 숫자 대신 ✔ 기호만 사용한다.
- 줄은 모바일 기준 18~25자 정도로 끊고, 문단 사이에는 빈 줄을 넣는다.
- 하나의 문단은 2~4줄 정도로 유지한다.
- 수치, 기간, 빈도, 기준, 비교 중 최소 4개를 넣는다.
- 키워드는 본문에서 3~5회만 자연스럽게 넣는다.

소제목 방향은 아래 흐름을 참고하되 그대로 복사하지 말고 키워드에 맞게 바꾼다.
${sectionDirection}

## 5번 섹션 제품 연결 방식
5번 섹션은 아래 순서로 작성한다.
1. 키워드 관련 건강 상태를 부드럽게 정리한다.
2. 꾸준한 생활 관리가 중요하다는 흐름을 만든다.
3. 마지막 2~3문장에서 한려담원 흑염소진액을 하나의 선택지로 짧게 언급한다.
4. 마지막 문장은 제품이 아니라 독자 건강을 응원하며 끝낸다.

제품 연결 예시 방향:
"이런 부분이 신경 쓰이신다면 한려담원 흑염소진액처럼
원료 균형을 보고 고르는 제품도 하나의 선택지가 될 수 있어요."

예시는 방향만 참고하고 그대로 복사하지 않는다.

## 댓글 규칙
- [댓글] 섹션에는 본댓글 ${mainCommentCount}개와 대댓글 ${replyCount}개를 만든다.
- 본댓글은 [댓글1]부터 순서대로 쓴다.
- 대댓글은 [작성자-N], [댓글러-N], [제3자-N] 중 하나를 사용하고 실제 존재하는 댓글 번호에 붙인다.
- 각 댓글은 한 줄로 쓴다.
- 댓글도 존댓말만 사용한다.
- 댓글 전체에서 "한려담원"은 1~2회만 자연스럽게 등장한다.
- 댓글에서는 [댓글3] 이후에 한려담원이 나오게 한다.
- 한려담원 언급은 추천/권유가 아니라 경험담, 질문, 선택 기준 대화로만 쓴다.
- 브랜드 추천, 구매 유도, 링크 요청, 병원명 요청 금지.
- "저도", "맞아요", "좋은 정보" 시작은 각각 최대 1개만 허용한다.

댓글 구성 참고:
${commentDirections.map((direction) => `- ${direction}`).join('\n')}

## 금지
- 마크다운 헤더, 굵게, 표, 코드블록, HTML
- 출력 태그 외 대괄호 라벨
- URL, 도메인, 출처 목록
- 치료 효과 단정, 질병 개선 단정
- 실제로 확인하지 않은 최신 날짜, 이벤트, 할인 정보

바로 [제목]부터 출력한다.`;
};
